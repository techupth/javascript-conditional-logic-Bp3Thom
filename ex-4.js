// Switch Statement
let lightBulbStatus = "On";

//Start coding here
switch (lightBulbStatus) {
    case "On": console.log("On");
    break;
    case "Off": console.log("Off");
    break;
    case "Broken": console.log("Light bulb is Broken.");
    break;
    default: console.log("Please choose the correct input");
}